export const getJobs = () => {
  function getWellDistributedRandom(min = 1, max = 6) {
    const sample1 = min + Math.random() * (max - min);
    const sample2 = min + Math.random() * (max - min);
    const sample3 = min + Math.random() * (max - min);

    return Number(((sample1 + sample2 + sample3) / 3).toFixed(3));
  }

  return new Promise<typeof allJobs>((res) => {
    setTimeout(() => {
      res(allJobs);
    }, 1000 * getWellDistributedRandom());
  });
};
